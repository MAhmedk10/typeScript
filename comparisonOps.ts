// THERE ARE 8 types of COMPARISON OPS.
// == equal to
// != not equal to
// === equal to and type
// !== not equal to and type
// > greater then
// < less than
// >= greater then or equal to
// <= less than and equal to

let num1 = 4
let num2 = 8

console.log(num1 == num2); // false

console.log(num1 != num2); // true

console.log(num1 === num2); // false

console.log(num1 !== num2); // true

console.log(num1 < num2); // true

console.log(num1 > num2); // false

console.log(num1 <= num2); // true

 console.log(num1 >= num2); // false