// THERE ARE 5 TYPES OF ASSIGMENT OPERATOR
let k = 12; // = operator
k += 4; // += operator
console.log(k); // 16

k -= 8; // -= operator
console.log(k);

k *= 4; // *= operator
console.log(k);

k %= 6; // %= operator
console.log(k);

k /= 8; // /= operator
console.log(k);

k **= 4; // **= operator
console.log(k);